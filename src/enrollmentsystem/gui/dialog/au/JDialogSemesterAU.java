    private void isSemesterUpdated() {
        Semester c = new Semester(this.semester.id(), jTextFieldSemester.getText());
        if ((c.semester().length() == 0) || (c.semester().length() == 0)) {
            jButton1.setEnabled(false);
            return;
        }
        if (this.semester.equals(c)) {
            jButton1.setEnabled(false);
        } else {
            jButton1.setEnabled(true);
        }
    }
