    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (jButton1.getText().equals("Save")) {
            int response = JOptionPane.showConfirmDialog(mainFrame, "Are you sure to add " + jTextFieldSemester.getText() + " ?", "Add time confirmation", JOptionPane.YES_NO_OPTION);
            if (response == JOptionPane.YES_OPTION) {
                Semester sy = new Semester(0, jTextFieldSemester.getText());
                new AppQuery().addSemester(sy);
                this.dispose();
            }
        } else if (jButton1.getText().equals("Update")) {
            int response = JOptionPane.showConfirmDialog(mainFrame, "Are you sure to update " + jTextFieldSemester.getText() + " ?", "Add time confirmation", JOptionPane.YES_NO_OPTION);
            if (response == JOptionPane.YES_OPTION) {
                Semester sy = new Semester(this.semester.id(), jTextFieldSemester.getText());
                new AppQuery().updateSemester(sy);
                this.dispose();
            }
        } else if (jButton1.getText().equals("Delete")) {
            int response = JOptionPane.showConfirmDialog(mainFrame, "Are you sure to delete " + jTextFieldSemester.getText() + " ?", "Add time confirmation", JOptionPane.YES_NO_OPTION);
            if (response == JOptionPane.YES_OPTION) {
                Semester sy = new Semester(this.semester.id(), jTextFieldSemester.getText());
                new AppQuery().deleteSemester(sy);
                this.dispose();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

