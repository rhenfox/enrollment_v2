    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (jButton1.getText().equals("Save")) {
            int response = JOptionPane.showConfirmDialog(mainFrame, "Are you sure to add " + jTextFieldRoom.getText() + " ?", "Add room confirmation", JOptionPane.YES_NO_OPTION);
            if (response == JOptionPane.YES_OPTION) {
                Room room = new Room(null, jTextFieldRoom.getText(), jTextFieldLocation.getText());
                new AppQuery().addRoom(room);
                this.dispose();
            }
        } else if (jButton1.getText().equals("Update")) {
            int response = JOptionPane.showConfirmDialog(mainFrame, "Are you sure to update " + jTextFieldRoom.getText() + " ?", "Add room confirmation", JOptionPane.YES_NO_OPTION);
            if (response == JOptionPane.YES_OPTION) {
                Room room = new Room(this.room.id(), jTextFieldRoom.getText(), jTextFieldLocation.getText());
                new AppQuery().updateRoom(room);
                this.dispose();
            }
        } else if (jButton1.getText().equals("Delete")) {
            int response = JOptionPane.showConfirmDialog(mainFrame, "Are you sure to delete " + jTextFieldRoom.getText() + " ?", "Add room confirmation", JOptionPane.YES_NO_OPTION);
            if (response == JOptionPane.YES_OPTION) {
                Room room = new Room(this.room.id(), jTextFieldRoom.getText(), jTextFieldLocation.getText());
                new AppQuery().deleteRoom(room);
                this.dispose();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

