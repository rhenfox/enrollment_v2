  private void isCourseUpdated() {
        Room c = new Room(this.room.id(), jTextFieldRoom.getText(), jTextFieldLocation.getText());
        if ((c.room().length() == 0) || (c.location().length() == 0)) {
            jButton1.setEnabled(false);
            return;
        }
        if (this.room.equals(c)) {
            jButton1.setEnabled(false);
        } else {
            jButton1.setEnabled(true);
        }
    }

